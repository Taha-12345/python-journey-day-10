#For Loop Basics

# Loop with range
for i in range(1, 6):
    print("Number:", i)

print("-" * 30)

# Loop through string
name = "Python"
for ch in name:
    print(ch)

print("-" * 30)

# Loop through list
fruits = ["Apple", "Banana", "Mango"]
for fruit in fruits:
    print(fruit)
